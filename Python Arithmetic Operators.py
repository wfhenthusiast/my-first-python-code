#Python Arithmetic Operators
#Additon (penjumlahan)
x=10
y=3
hasil = x+y
print(hasil)

#Subtraction
hasil = x-y
print(hasil)

#Multiplication
hasil = x*y
print(hasil)

#Division
hasil = x/y
print(hasil)

#Modulus
hasil = x%y
print(hasil)

#Exponentiation
#sama seperti x dipangkatkan sebanyak y
hasil = x ** y
print(hasil)

#Floor Division
hasil = x // y
print(hasil)

#Tipe Data
#list
daftar_siswa = [
    "tono",
    "supri",
    "susana",
    "siapa"
]
print(daftar_siswa)

#slicing index
#tanda koma dalam list menandakan index atau nomor urut yang dimulai dari 0
print(daftar_siswa[:3])