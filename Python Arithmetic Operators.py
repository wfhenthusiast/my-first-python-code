#Python Arithmetic Operators
#Additon
x=10
y=3
hasil = x+y
print(hasil)

#Subtraction
hasil = x-y
print(hasil)

#Multiplication
hasil = x*y
print(hasil)

#Division
hasil = x/y
print(hasil)

#Modulus
hasil = x%y
print(hasil)

#Exponentiation
#sama seperti x dipangkatkan sebanyak y
hasil = x ** y
print(hasil)

#Floor Division
hasil = x // y
print(hasil)